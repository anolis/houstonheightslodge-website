        Volunteer activities that the Odd Fellows have helped with at the cemetery have included
        cleanup days, headstone restoration workshops, and Juneteenth celebrations.
      </p>
  
      <p>From the historical marker at the cemetery:</p>
  
      <blockquote>
        <p>
          This cemetery served the early African-American community in Houston for approximately 100 years.
          The Olivewood Cemetery Association incorporated in 1875 and purchased 5.5 acres of this property
          that same year from Elizabeth Morin Slocomb. The organization bought two adjacent acres in 1917.
          Also known in its early years as olive wood, hollow wood and hollywood, it is one of the oldest
          known platted cemeteries in the city. The original 444 family plots comprising over 5,000 burial
          spaces were laid out along an elliptical drive. The burial ground contains several hundred marked
          graves, in addition to an unknown number of unmarked graves.
        </p>
  
        <p>
          Interred here are pivotal leaders of Houston's post emancipation African-American community,
          including the pastor of Trinity Methodist Episcopal Church, the Rev. Elias Dibble; Businessman
          James B. Bell; Alderman and Landowner Richard Brock; Attorney J. Vance Lewis; Educator James D.
          Ryan; Physician Russell F. Ferrill; and Dentist Milton A. Baker. Also buried here are ex-slaves,
          laborers, sororal and fraternal organization members, and military veterans.
        </p>
  
        <p>
          This cemetery features obelisks, statuary, curbing and interior fencing. The burial ground also
          includes examples of preemancipation burial practices, including upright pipes (symbolizing the
          path between the worlds of the living and the dead), ocean shells as grave ornaments and text
          containing upside down or backwards letters (as used in some West African cultures to signify
          death). Today, Olivewood Cemetery remains as a key historical site in Houston, serving as a
          testament to the foresight and perseverance of the cemetery founders.
        </p>
      </blockquote>
