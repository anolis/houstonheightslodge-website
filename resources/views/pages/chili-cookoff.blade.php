<p>15th Annual Chili Cook-off
Saturday, February 4th , noon to 3 pm
Hello brothers and sisters! Are you ready to dust off your chili spoons? Oh, I know you are. Come One, Come All! Come Hungry!
The Houston Heights Odd Fellows Lodge 225 is hosting our 15th Annual Chili Cook-Off! Funds raised will support Lodge activities and charities. Family-friendly so bring the kids!
Tasting fee is just Five Bucks! So, you can try all teams and help us choose The People’s Choice Award!
This gets you a spoon, cups, and a wristband.
Water, beer, and soft drinks available for a donation
Tables are available inside and outside of the lodge.
Chili categories:
Traditional without Beans
Traditional with Beans
People’s Choice
3 Trophies will be awarded!
We also have space for teams! Contact Natasha Thornton by email narensthornton@gmail.com if you would like to sign your team up.
Here are the team rules
TEAM RULES
-$25 entry fee for a 10 x 10 space (about the size of a pop-up tent). Spaces are available on a first come first served basis, starting at 8 a.m.
-2 Members per team will receive a complimentary wristband. All other members of a team, please purchase a $5 wristband for the Odd Fellows Chili Cook-Off Fundraiser.
-Teams must supply all chili ingredients, equipment, and fuel. No electricity will be available on site. Bring tables, cookers, pots, pans, serving utensils, and a pop-up tent, if desired. Decorating your space is encouraged but NOT required.
-All preparation for the Competition Chili must be done on-site and from scratch. Scratch means starting with raw meat, vegetables, and regular spices. Canned tomatoes are allowed. Meat and vegetables may be chopped beforehand. You may mix spices before you arrive.
-Must prepare 3 Gallons of Chili.
-Must save 2 cups for judging starting at 1:30 p.m.
-Set up begins between 8 and 9 a.m.
-Must be prepared to start serving by 12 Noon for People's Choice.
-No Bowls please. We do not have a garbage disposal; so no cleaning pots on site.
4 oz. Dixie cups for patrons and judging, spoons, and napkins provided by Odd Fellows Lodge #225.
Once again if interested in signing up a team, contact Natasha Thornton at narensthornton@gmail.com
See yall there!!
